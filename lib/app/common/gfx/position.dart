/// [GFXPosition] is used to position the icon, badges to start or end of the button
/// See GFButton and GFButtonBadge
enum GFXPosition {
  /// [GFXPosition.start] is used to place icon, badges to start of the GFButton and GFButtonBadge
  start,

  /// [GFXPosition.end] is used to place icon, badges to end of the GFButton and GFButtonBadge
  end,
}
